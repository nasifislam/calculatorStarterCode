function multiply (a,b){
    return a*b
}

function divide (n,m){
    return n/m
}

function subtract (a,b){
    return a-b
}
function add (m,d){
    return m-d
}

function power(a,g){
    return Math.pow(a,g)
}

function square_root(i,s){
    return Math.sqrt(i,s)
}

function logBase10(d){
    return Math.log10(d)
}

function generateRandomNumber(){
    return Math.random()
}
